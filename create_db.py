def create_db():
    conn = sqlite3.connect('medical_terms.db')
    c = conn.cursor()
    c.execute('''
        CREATE TABLE IF NOT EXISTS terms (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            term TEXT UNIQUE NOT NULL,
            definition TEXT NOT NULL
        )
    ''')
    conn.commit()

    # 既存データがあればクリア
    c.execute('DELETE FROM terms')
    conn.commit()

    for term, definition in medical_terms.items():
        c.execute('INSERT INTO terms (term, definition) VALUES (?, ?)', (term, definition))
    conn.commit()
    conn.close()
    print("DB作成とデータ挿入完了")

if __name__ == '__main__':
    create_db()
